<h4>About the project</h4>
<hr>
<p>
    Cytra is going to be a vr game highly based on the anime vrmmos that stuff like Sword Art Online has. Currently
    I am developing a speech to incantation type system where you talk into a mic and it transcribe what you say and
    sees if there is a spell with that incantation. This game isn't fully thought out, but I really want one of the
    core aspects of this game to be the speech to cast system. I feel like it will immerse the player even more in the
    world and just create a amazing experience.
</p>
